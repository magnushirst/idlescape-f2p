    private final int id;
    private final String displayName;
    private final BooleanSupplier meetsRequirements;

    WeaponItemRef(int id) {
        this(id, "", () -> true);
    }

    WeaponItemRef(int id, String displayName) {
        this(id, displayName, () -> true);
    }

    WeaponItemRef(int id, BooleanSupplier meetsRequirements) {
        this(id, "", meetsRequirements);
    }

    public String getName() {
        return name();
    }

    public boolean matches(String text) {
        return text != null && text.equalsIgnoreCase(displayName);
    }

    public static WeaponItemRef fromID(int id) {
        for (WeaponItemRef itemRef : values()) {
            if (itemRef.id == id) {
                return itemRef;
            }
        }
        return null;
    }

    public static int notedId(GenericItemRef ref) {
        return ref.getId() + 1;
    }

    public static WeaponItemRef fromName(String name) {
        if (name == null) {
            return null;
        }
        for (WeaponItemRef itemRef : values()) {
            if (itemRef.name().equalsIgnoreCase(name)) {
                return itemRef;
            }
        }
        return null;
    }

    public static WeaponItemRef getById(int id) {
        return fromID(id);
    }

    @Override
    public boolean getMeetsRequirements() {
        return meetsRequirements.getAsBoolean();
    }

    @Override
    public int[] getIds() {
        if (this.getDisplayName() == null || this.getDisplayName().isEmpty()) {
            return new int[] {this.getId()};
        }
        return Arrays.stream(GenericItemRef.values())
                .filter(item -> item.getDisplayName() != null)
                .filter(item -> Objects.equals(item.getDisplayName(), this.displayName))
                .mapToInt(GenericItemRef::getId)
                .toArray();
    }

    @Override
    public GenericItemRef[] getSimilarItems() {
        if (this.getDisplayName() == null || this.getDisplayName().isEmpty()) {
            return new GenericItemRef[] {this};
        }
        return Arrays.stream(GenericItemRef.values())
                .filter(item -> item.getDisplayName() != null)
                .filter(item -> Objects.equals(item.getDisplayName(), this.displayName))
                .toArray(GenericItemRef[]::new);
    }

    public boolean isWearingSimilar() {
        return equipmentContainsAny(this.getSimilarItems());
    }

    public static GenericItemRef[] getBlackjacks() {
        return new GenericItemRef[] {OAK_BLACKJACK, WILLOW_BLACKJACK, MAPLE_BLACKJACK};
    }

    public static boolean isTwoHanded(GenericItemRef itemRef) {
        if (itemRef == null) {
            return false;
        }

        WeaponItemRef weaponItemRef =
                itemRef instanceof WeaponItemRef ? (WeaponItemRef) itemRef : fromID(itemRef.getId());
        if (weaponItemRef != null) {
            return weaponItemRef.isTwoHanded();
        }

        return Arrays.stream(itemRef.getSimilarItems())
                .mapToInt(GenericItemRef::getId)
                .mapToObj(WeaponItemRef::fromID)
                .filter(Objects::nonNull)
                .anyMatch(ref -> ref.isTwoHanded());
    }

    public boolean isTwoHanded() {
        return Arrays.stream(TwoHandedSlotRef.values())
                .anyMatch(twoHandedRef -> twoHandedRef.getItemRef() == this
                        || Arrays.stream(twoHandedRef.getSimilarItems()).anyMatch(item -> item.getId() == this.id));
    }
