    private final int id;
    private final String displayName;
    public final BooleanSupplier meetsRequirements;

    ItemRef(int id) {
        this(id, "", () -> true);
    }

    ItemRef(int id, String displayName) {
        this(id, displayName, () -> true);
    }

    ItemRef(int id, BooleanSupplier meetsRequirements) {
        this(id, "", meetsRequirements);
    }

    public boolean getMeetsRequirements() {
        return meetsRequirements.getAsBoolean();
    }

    public boolean matches(String text) {
        return text != null && text.equalsIgnoreCase(displayName);
    }

    public static ItemRef fromID(int id) {
        for (ItemRef gi : values()) if (gi.id == id) return gi;
        return null;
    }

    public int[] getIds() {
        if (this.getDisplayName() == null || this.getDisplayName().isEmpty()) {
            return new int[] {this.getId()};
        }
        return Arrays.stream(ItemRef.values())
                .filter((item) -> item.getDisplayName() != null)
                .filter(item -> Objects.equals(item.displayName, this.displayName))
                .mapToInt(ItemRef::getId)
                .toArray();
    }

    public ItemRef[] getSimilarItems() {
        if (this.getDisplayName() == null || this.getDisplayName().isEmpty()) {
            return new ItemRef[] {this};
        }
        return Arrays.stream(ItemRef.values())
                .filter((item) -> item.getDisplayName() != null)
                .filter(item -> Objects.equals(item.displayName, this.displayName))
                .toArray(ItemRef[]::new);
    }

    public static ItemRef getById(int id) {
        return Arrays.stream(ItemRef.values())
                .filter(item -> item.getId() == id)
                .findFirst()
                .orElse(null);
    }
